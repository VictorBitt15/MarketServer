public class Produto {
    private String name;
    private Double preco;

    public String getName() {
        return name;
    }

    public Double getPreco() {
        return preco;
    }

    public void setName(String name) {
        this.name = name;
    }
    public void setPreco(Double preco) {
        this.preco = preco;
    }
}
